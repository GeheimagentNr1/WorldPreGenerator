package de.geheimagentnr1.world_pre_generator.elements.queues.tasks.pregen.data;

public class WorldPos {
	
	
	private final int x;
	
	private final int z;
	
	public WorldPos( int _x, int _z ) {
		
		x = _x;
		z = _z;
	}
	
	public int getX() {
		
		return x;
	}
	
	public int getZ() {
		
		return z;
	}
}
